#!/bin/bash

# Запуск тестов
echo "Тестирование программы..."

# Функция для выполнения теста
run_test() {
    input="\$1"
    expected_output="\$2"

    # Запуск программы с вводом и фильтрация лишних строк
    output=$(echo -e "$input" | ./bubble_sort_program | grep -v "Введите количество элементов" | grep -v "Введите элементы массива")

    # Проверка результата
    if [ "$output" == "$expected_output" ]; then
        echo "Тест прошел успешно."
    else
        echo "Тест не пройден."
        echo "Ожидаемый вывод:"
        echo -e "$expected_output"
        echo "Фактический вывод:"
        echo "$output"
        exit 1
    fi
}

# Пример теста: проверка сортировки массива
run_test "5\n3\n1\n4\n2\n7\n" "Исходный массив: 
3 1 4 2 7 
Отсортированный массив: 
1 2 3 4 7"

# Пример теста: проверка массива с одинаковыми элементами
run_test "5\n7\n7\n7\n7\n7\n" "Исходный массив: 
7 7 7 7 7 
Отсортированный массив: 
7 7 7 7 7"

# Пример теста: проверка сортировки уже отсортированного массива
run_test "5\n1\n2\n3\n4\n5\n" "Исходный массив: 
1 2 3 4 5 
Отсортированный массив: 
1 2 3 4 5"

# Пример теста: проверка некорректного ввода (нецелое число)
output_invalid=$(echo -e "5\na\n1\n2\n3\n4\n5\n" | ./bubble_sort_program)
if echo "$output_invalid" | grep -q "Ошибка: введите целое число."; then
    echo "Тест некорректного ввода прошел успешно."
else
    echo "Тест некорректного ввода не пройден."
    exit 4
fi

# Пример теста: проверка превышения максимального размера массива
output_large_input=$(echo -e "105\n1\n2\n3\n4\n5\n" | ./bubble_sort_program)
if echo "$output_large_input" | grep -q "Ошибка: введите корректное положительное число (1-100)."; then
    echo "Тест превышения максимального размера массива прошел успешно."
else
    echo "Тест превышения максимального размера массива не пройден."
    exit 5
fi

# Вывод итогового результата
echo "Все тесты завершены."
